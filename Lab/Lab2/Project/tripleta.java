public class tripleta {
    private int id;
    private int dmg;

    public tripleta(int id, int dmg){
        this.id = id;
        this.dmg = dmg;
    }

    public int getId(){ return this.id; }
    public int getDmg(){ return this.dmg; }

    public void setId(int id){ this.id = id; }
    public void setDmg(int dmg){ this.dmg = dmg; }
}
